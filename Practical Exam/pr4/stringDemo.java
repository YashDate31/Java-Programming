class stringDemo
{
	public static void main(String args[])
	{
		String a = "Yash";
		System.out.println("Upper case:-"+ a.toUpperCase());
		System.out.println("Lower case:- "+ a.toLowerCase());
		System.out.println("Length"+ a.length());
		System.out.println("Char at 1"+ a.charAt(3));
		System.out.println("replace"+ a.replace("Y","y"));
		System.out.println("concat "+ a.concat(" Date"));
		System.out.println("Substrign"+ a.substring(2,3));
		
		
	}
}